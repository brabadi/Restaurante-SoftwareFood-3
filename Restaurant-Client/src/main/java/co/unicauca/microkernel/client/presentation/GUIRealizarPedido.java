    public void imgPedido(PlatoEjecutivo plato) {
        System.out.println("IMAGENNN" + plato.getFoto().toString());
        BufferedImage image = null;
        InputStream in = new ByteArrayInputStream(plato.getFoto());
        try {
            image = ImageIO.read(in);
        } catch (IOException ex) {
            Logger.getLogger(GUIRealizarPedido.class.getName()).log(Level.SEVERE, null, ex);
        }
        ImageIcon imgi = new ImageIcon(image.getScaledInstance(314, 314, 0));
        jLableIMGPlato.setIcon(imgi);
        jLableIMGPlato.setHorizontalAlignment(jLableIMGPlato.CENTER);
        jLableIMGPlato.setVerticalAlignment(jLableIMGPlato.CENTER);
    }

