    private void btnRealizarPedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarPedActionPerformed

        if ((txtCantidad.getText().length() > 0) && (Utilities.isNumeric(txtCantidad.getText()) && Integer.valueOf(txtCantidad.getText()) > 0)) {
            int total = 0;
            cantidad = Integer.valueOf(txtCantidad.getText());
            total = (this.plato.getPrecio()) * cantidad;

            int confirmado = JOptionPane.showConfirmDialog(null, "El valor total a Pagar es: " + total + "\n ¿Desea Continuar?", "Confirmar Pedido", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);;
            if (JOptionPane.OK_OPTION == confirmado) {
                int ID_ped = 0;
                try {
                    ID_ped = tamanioPed() + 1;
                } catch (Exception ex) {
                    Logger.getLogger(GUIRealizarPedido.class.getName()).log(Level.SEVERE, null, ex);
                }

                Calendar fecha = new GregorianCalendar();
                int año = fecha.get(Calendar.YEAR);
                int mes = fecha.get(Calendar.MONTH);
                int dia = fecha.get(Calendar.DAY_OF_MONTH);
                int hora = fecha.get(Calendar.HOUR_OF_DAY);
                int minuto = fecha.get(Calendar.MINUTE);

                String fecha_ped = (dia + "/" + (mes + 1) + "/" + año);
                String hora_ped = (hora + ":" + minuto);

                addPedido(ID_ped, Integer.valueOf(IDCliente), plato.getResNIT(), plato, cantidad, fecha_ped, hora_ped, PedEstado.CREADO, total);
               
            } else {
                JOptionPane.showMessageDialog(null, "Pedido Cancelado");
                
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe Digitar Una Cantidad Válida");
            
        }


    }//GEN-LAST:event_btnRealizarPedActionPerformed

