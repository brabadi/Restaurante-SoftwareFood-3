    private void addPedido(int ped_Id, int cli_Id, int res_Nit, PlatoEjecutivo plaEje_Dia, int ped_Cantidad, String ped_fecha, String ped_Hora, PedEstado ped_Estado, int ped_PrecioTotal) {
        IClientAccess service = new ClientAccessSocket();
        ClientService cliente = new ClientService(service);

        Pedido pedido = new Pedido(ped_Id, cli_Id, res_Nit, plaEje_Dia, ped_Cantidad, ped_fecha, ped_Hora, ped_Estado, ped_PrecioTotal);

        try {
            String resultComponent = service.savePedido(pedido);
            System.out.println(resultComponent);
            JOptionPane.showMessageDialog(null, "Pedido Realizado");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El Pedido Ingresado ya esta registrado" + e.getMessage());
        }
    }

    private int tamanioPed() throws Exception {
        IClientAccess service = new ClientAccessSocket();
        ClientService servicioRestaurante = new ClientService(service);
        List<Pedido> lsPedido = servicioRestaurante.listarPedido();

        int id = lsPedido.get(lsPedido.size() - 1).getPed_Id();
        return id;
    }


